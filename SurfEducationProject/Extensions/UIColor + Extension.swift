import Foundation
import UIKit

extension UIColor {
    static func warningRed() -> UIColor {
        .red
    }
    static func grayLight() -> UIColor {
        .lightGray
    }
    static func standartBlack() -> UIColor {
        .black
    }
    static func standartWhite() -> UIColor {
        .white
    }
}
